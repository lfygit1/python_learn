# fiddler 简介：
## Fiddler 工具详细介绍
"""
Fiddler 是一个功能强大的 Web 调试代理工具，主要用于捕获、检查、修改和调试 HTTP/HTTPS 网络流量。

### 主要功能

#### 1. 网络流量监控
- **实时捕获**：能够捕获所有通过本机的 HTTP 和 HTTPS 请求
- **完整记录**：显示每个请求的详细信息，包括请求头、响应头、请求体和响应体
- **时间分析**：展示每个请求的耗时情况，帮助分析性能瓶颈

#### 2. 请求调试与分析
- **断点调试**：可以在请求发送前或响应返回前设置断点，修改请求或响应内容
- **请求构建**：可以直接在工具内构造和发送自定义 HTTP 请求
- **会话重放**：可以重复发送已捕获的请求，方便测试

#### 3. 性能分析
- **瀑布图显示**：以可视化方式展示页面加载过程和各资源的加载时间
- **统计报告**：提供详细的网络请求统计信息
- **缓存分析**：检查缓存策略是否正确实施

### 使用场景

#### 接口测试
- 监控 API 请求和响应
- 验证请求参数和响应数据格式
- 检查 HTTP 状态码和错误信息

#### Web 开发调试
- 分析网页加载性能
- 查看 AJAX 请求详情
- 调试前后端交互问题

#### 安全测试
- 检查敏感信息传输
- 分析认证和授权机制
- 测试安全漏洞

### 界面组成

#### 1. 会话列表（左上）
显示所有捕获的 HTTP 请求，每行代表一个会话，包含方法、URL、状态码、大小和耗时等基本信息。

#### 2. 详细信息面板（右上）
显示选中会话的详细信息，包括：
- **Headers**：请求和响应头信息
- **TextView**：原始文本视图
- **WebForms**：表单数据显示
- **JSON**：JSON 格式化显示
- **Cookies**：Cookie 信息

#### 3. 统计面板（底部）
显示会话的时间线和性能统计数据。

### 特殊功能

#### HTTPS 解密
- 可以解密 HTTPS 流量以便查看加密内容
- 需要安装 Fiddler 根证书
- 支持解密特定域名或所有 HTTPS 流量

#### 自动响应
- 可以为特定请求返回预设的响应
- 在开发过程中模拟不同服务器响应状态
- 支持从本地文件返回响应内容

#### 手机抓包
- 可以捕获移动设备的网络流量
- 需要在手机上配置代理指向运行 Fiddler 的电脑
- 对移动端应用调试非常有用

Fiddler 是 Web 开发者、测试工程师和安全研究人员的重要工具，它提供了深入洞察应用程序网络行为的能力。
"""




# 配置 Fiddler
# .PC端配置
# 如果只是监听本机HTTP数据包的话不需要做任何配置，打开即可使用
# .手机端配置
# 在 Fiddler 中，打开菜单 Tools > Options > Connections ，输入监听端口（默认8888）
# 勾选 "Allow remote computers to connect"  并重启fiddler使配置生效
# 保证手机和电脑处于同一局域网内，在手机上，打开设置中的wifi高级设置，选择手动设置，输入fiddler主机（电脑）的ip地址和端口号（默认8888）

# 抓包https配置：
# fiddler抓包https 时候涉及到证书及数据加密的问题，需要做一下其他配置：
# 在 Fiddler 中，打开菜单 Tools > Options > HTTPS
# 勾选 "Decrypt HTTPS traffic"
# 在弹出的证书安装提示中，点击 "Yes"
# 选择证书存储位置（建议选择"受信任的根证书颁发机构"）
# 点击 "OK" 保存设置
# 重启 Fiddler