# http相关协议简介
# 1.http协议工作原理
#   浏览器向服务器发送请求，服务器返回响应。
#   浏览器接收到响应，将响应内容显示在页面上。
# 2.http协议请求方法:
#   GET:获取资源
#   POST:提交数据
#   PUT:更新资源
#   DELETE:删除资源
#   HEAD:获取资源
# 3.http状态码:
#   1xx:表示请求已被服务器接收，继续处理中
#   2xx:表示请求成功，服务器已接收、理解 并 接受请求
#   3xx:重定向，表示需要后续的操作才能完成请求
#   4xx:客户端请求错误，请求中包含词法错误或者无法被执行
#   5xx:服务器错误，服务器在处理某个正确的请求时发生错误
