#41、列表生成字符串，用for循环实现,最后的结果为:我@在@哪@我@是@谁
l1 = ['我', '在', '哪', '我', '是', '谁']

result = ""
for i in range(len(l1)):
    result += l1[i]
    # 不是最后一个元素，才加 '@'
    if i != len(l1) - 1:
        result += "@"

print(result)



